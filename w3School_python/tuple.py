# Python Tuples

# tuple allow Duplicates
thistuple = ("apple", "banana", "cherry", "apple", "cherry")
print(thistuple)

# tuple length
print(len(thistuple))

# Create Tuple With One Item

# bukan tuple
thistuple1 = ("apple",)
print(type(thistuple1))

# not a tuple
thistuple2 = ("apple1")
print(type(thistuple2))

