# list
# list menggunakan kurung siku
data_list = ['ucup', 'otong', 'joko', 'wati', 44, 66, 22, 'Romusha']
print(data_list)

# tuple
# tuple menggunakan kurung biasa
data_tuple = ('ucup', 'otong', 'joko', 'wati', 44, 66, 22, 'Romusha')
print(data_tuple)
print(data_tuple[0])

# set
# set menggunakan kurung siku
data_set = {'ucup', 'otong', 'joko', 'wati', 44, 66, 22, 'Romusha'}
print(data_set)

data_set.add('budi')
print(data_set)

data_set.remove('ucup')
print(data_set)